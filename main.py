def duh2eng(arg:str) -> str:
    result = ''
    for i in arg:
        code = ord(i)
        if code >= 0xac00 and code <= 0xd7a3:
            code -= 0xac00
            result += 초성[code // (len(중성) * len(종성))] + 중성[(code % (len(중성) * len(종성))) // len(종성)] + 종성[code % len(종성)]
        else:
            if i in 자모음:  result += 자모음conv[자모음.index(i)]
            else:  result += i
    return result

